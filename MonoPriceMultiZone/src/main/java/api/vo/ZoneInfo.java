package api.vo;

public class ZoneInfo {
	private String zone;
	private String zoneName;
	private String paControl;
	private String powerControl;
	private String muteControl;
	private String dtControl;
	private String volumeControl;
	private String trebleControl;
	private String bassControl;
	private String balanceControl;
	private String sourceControl;
	private String keypadConnect;
	
	public String getZone() {
		return zone;
	}
	public void setZone(String zone) {
		this.zone = zone;
	}
	public String getZoneName() {
		return zoneName;
	}
	public void setZoneName(String zoneName) {
		this.zoneName = zoneName;
	}
	public String getPaControl() {
		return paControl;
	}
	public void setPaControl(String paControl) {
		this.paControl = paControl;
	}
	public String getPowerControl() {
		return powerControl;
	}
	public void setPowerControl(String powerControl) {
		this.powerControl = powerControl;
	}
	public String getMuteControl() {
		return muteControl;
	}
	public void setMuteControl(String muteControl) {
		this.muteControl = muteControl;
	}
	public String getDtControl() {
		return dtControl;
	}
	public void setDtControl(String dtControl) {
		this.dtControl = dtControl;
	}
	public String getVolumeControl() {
		return volumeControl;
	}
	
	public void setVolumeControl(String volumeControl) {
		this.volumeControl = volumeControl;
	}
	public String getTrebleControl() {
		return trebleControl;
	}
	public void setTrebleControl(String trebleControl) {
		this.trebleControl = trebleControl;
	}
	public String getBassControl() {
		return bassControl;
	}
	public void setBassControl(String bassControl) {
		this.bassControl = bassControl;
	}
	public String getBalanceControl() {
		return balanceControl;
	}
	public void setBalanceControl(String balanceControl) {
		this.balanceControl = balanceControl;
	}
	public String getSourceControl() {
		return sourceControl;
	}
	public void setSourceControl(String sourceControl) {
		this.sourceControl = sourceControl;
	}
	public String getKeypadConnect() {
		return keypadConnect;
	}
	public void setKeypadConnect(String keypadConnect) {
		this.keypadConnect = keypadConnect;
	}
}
